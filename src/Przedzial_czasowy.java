import java.util.Calendar;

public class Przedzial_czasowy
{
    Calendar pierwszy_dzien_pobytu, ostatni_dzien_pobytu;

    public Przedzial_czasowy(Calendar pierwszy_dzien_pobytu, Calendar ostatni_dzien_pobytu)
    {
        this.pierwszy_dzien_pobytu = pierwszy_dzien_pobytu;
        this.ostatni_dzien_pobytu = ostatni_dzien_pobytu;
    }
}
